/**
 * Image files are placed in: public/products
 * Your screenshot shows names like 1, 1.1, 1.2...
 * If the real files are PNG/WEBP or have no extension, change PRODUCT_IMAGE_EXTENSION only.
 */
const PRODUCT_IMAGE_EXTENSION = ".webp";

const productImage = (fileName: string) =>
  `/products/${fileName}${PRODUCT_IMAGE_EXTENSION}`;

const slugify = (value: string) =>
  value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");

const makeProduct = (
  id: number,
  name: string,
  category: string,
  model: string,
  shortDescription: string,
  fullDescription: string,
  specifications: Record<string, string>,
  galleryFiles: string[],
  applications: string[],
  packagingInfo: string,
  availabilityStatus: Product["availabilityStatus"] = "In Stock",
): Product => {
  const gallery = galleryFiles.map(productImage);

  return {
    id,
    slug: slugify(name),
    name,
    category,
    model,
    shortDescription,
    fullDescription,
    specifications,
    image: gallery[0],
    gallery,
    applications,
    packagingInfo,
    availabilityStatus,
  };
};

